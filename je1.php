      <p><em><strong>O PHP</strong></em> como é conhecido hoje, é na verdade o 
	sucessor para um produto chamado PHP/FI. Criado em 1994 por Rasmus Lerdof,
	a primeira encarnação do PHP foi um simples conjunto de binários Common 
	Gateway Interface (CGI) escrito em linguagem de programação C. Originalmente 
	usado para acompanhamento de visitas para seu currículo online, ele nomeou o 
	conjunto de scripts de "Personal Home Page Tools" mais frequentemente referenciado
	como "PHP Tools." Ao longo do tempo, mais funcionalidades foram desejadas, e Rasmus
	reescreveu o PHP Tools, produzindo uma maior e rica implementação. Este novo modelo 
	foi capaz de interações com Banco de Dados e mais, fornecendo uma estrutura no qual
	os usuários poderiam desenvolver simples e dinâmicas aplicações web, como um livros 
	de visitas. Em Junho de 1995, Rasmus » liberou o código fonte do PHP Tools para o 
	público, o que permitiu que desenvolvedores usassem da forma como desejassem. Isso
	permitiu - e encorajou - usuários a fornecerem correções para bugs no código, e em geral, aperfeiçoá-lo.</p>

<p>Em Setembro do mesmo ano, Rasmus expandiu o PHP e - por um breve período - 
mudou o nome PHP. Agora referindo-se a ferramenta como FI, abreviação para "Forms 
Interpreter", a nova implementação incluiu algumas funcionalidades básicas do PHP 
como bem conhecemos hoje. Tinha variáveis no estilo Perl, interpretação automática de 
variáveis de formulários, e sintaxe HTML embutida. A sintaxe em si era muito similar com 
a do Perl, porém muito mais limitada, simples, e um pouco inconsistente. De fato, para 
embutir o código em um arquivo HTML, desenvolvedores tinham que usar comentários HTML. 
Embora este método não sido inteiramente bem-recebido, FI continuou a desfrutar um crescimento e
 aceitação como uma ferramente CGI --- mas ainda não como uma linguagem. Contudo, isso começou a
 mudar no mês seguinte; em Outubro, 1995 Rasmus liberou um completa reescrita do código. Trazendo
 de volta o nome PHP, estava agora (brevemente) nomeado "Personal Home Page Contruction Kit" e foi
 o primeiro lançamento a vangloriar-se que era, na época, considerado um avançado script de interface. 
 A linguagem foi desenvolvida para, deliberadamente, ser parecida com C, tornando-a fácil para ser 
 adotada por desenvolvedores habituados com C, Perl e linguagens similares. Tendo sido até este 
 momento exclusiva para sistemas UNIX e sistemas compatíveis com POSIX, o potencial para uma 
 implementação em um Windows NT começava a ser explorada.</p>



